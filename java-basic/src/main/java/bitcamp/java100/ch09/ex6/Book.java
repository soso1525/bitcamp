package bitcamp.java100.ch09.ex6;

public class Book {
    String title;
    String author;
    String press;
    int page;
    int price;

    public Book() {

    }

    public Book(String title, String author, String press, int page, int price) {
        this.title = title;
        this.author = author;
        this.press = press;
        this.page = page;
        this.price = price;
    }
}
